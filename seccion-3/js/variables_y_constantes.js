//CON LET PUEDO CAMBIAR EL VALOR DE LA VARIABLE, PERO CON CONST NO PUEDO CAMBIAR EL VALOR DE LA CONSTANTE

//Let sirve para declarar variables que pueden cambiar su valor

let edad = 30;
console.log(edad);

edad = 31;
console.log(edad);

//const sirve para declarar constantes, es decir, variables que no pueden cambiar su valor

const pi = 3.1416;
console.log(pi);

pi = 3.14; 
console.log(pi);

